<script setup>
import { ref } from '@vue/composition-api';

const props = defineProps({
  /**
   * 是否显示搜索条件
   */
  showSearch: {
    type: Boolean,
    default: true,
  },
  /**
   * 所有的列
   */
  columns: {
    type: Array,
  },
})

const emits = defineEmits(['update:showSearch', 'queryTable']);
/**
 * 显隐数据
 */
const value = ref([]);
/**
 * 弹出层标题
 */
const title = ref("显示/隐藏");
/**
 * 是否显示弹出层
 */
const open = ref(false);


/**
 * 搜索
 */
const toggleSearch = () => {
  emits("update:showSearch", !props.showSearch);
}
/**
 * 刷新
 */
const refresh = () => {
  emits("queryTable");
}
/**
 * 右侧列表元素变化
 */
const dataChange = (data) => {
  for (let item in props.columns) {
    const key = props.columns[item].key;
    props.columns[item].visible = !data.includes(key);
  }
}
/**
 * 打开显隐列dialog
 */
const showColumn = () => {
  open.value = true;
}

// 显隐列初始默认隐藏列
for (let item in props.columns) {
  if (props.columns[item].visible === false) {
    value.value.push(parseInt(item));
  }
}

</script>
<template>
  <div class="top-right-btn" v-on="$listeners">
    <el-row>
      <el-tooltip
        class="item"
        effect="dark"
        :content="props.showSearch ? '隐藏搜索' : '显示搜索'"
        placement="top"
      >
        <el-button circle icon="el-icon-search" @click="toggleSearch()" />
      </el-tooltip>
      <el-tooltip class="item" effect="dark" content="刷新" placement="top">
        <el-button circle icon="el-icon-refresh" @click="refresh()" />
      </el-tooltip>
      <el-tooltip class="item" effect="dark" content="显隐列" placement="top" v-if="columns">
        <el-button circle icon="el-icon-menu" @click="showColumn()" />
      </el-tooltip>
    </el-row>
    <el-dialog :title="title" :visible.sync="open" append-to-body>
      <el-transfer
        class="my-el-transfer"
        :titles="['显示', '隐藏']"
        v-model="value"
        :data="props.columns"
        @change="dataChange"
      ></el-transfer>
    </el-dialog>
  </div>
</template>

